    <p><a name="12"></a><b><font size="2" color="#800000">What are regulations on Foreign Diploma and Credit Recognition in the 
    USA?</font></b></p>
    <p><br>
    The United States is a federal republic with a decentralized system of 
    education. Degree, Diploma, and Credit recognition are not performed or 
    regulated by the federal government. Within the US, no government agency 
    monitors the Evaluation of Educational Credentials completed outside the 
    country.</p>
    <p>
    <a class="sublink" target="_blank" href="http://www.ed.gov/NLE/USNEI/us/natcouncil-intro.html">National Council for the Recognition of Foreign Academic Credentials</a> 
    is the U.S. national body responsible for developing and publishing 
    comparability statements designed to assist U.S. educators and credential 
    evaluators in determining how international qualifications correspond to US 
    Certificates, Diplomas, and Degrees at various educational levels.</p>
    <p>
    <a class="sublink" target="_blank" href="http://www.ed.gov/NLE/USNEI/us/inst.html">U.S. Institutions and Programs</a> provides directories of American Schools, 
    Colleges, Universities, and other accredited educational providers who are 
    the competent authorities for recognition and placement decisions having to 
    do with admission to study in the United States.</p>
    <p>Regulation of Professions provides information and links regarding 
    qualifying and becoming licensed to practice regulated occupations, which is 
    a state-level responsibility.</p>
    <p>The competent authority is the hiring employer for purposes other than 
    enrolling as a student or working in a regulated profession.</p>
    <p>
    <a class="sublink" target="_blank" href="http://www.ed.gov/NLE/USNEI/us/uscompauth.html">Overview and Competent Authorities</a> 
    defines the legal competent authorities in the United States for various 
    kinds of recognition.</p>
