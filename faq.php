    <br>
    <font color="#0000FF"><i>Therefore order:</i></font><br>
    <a class="sublink" href="services_cce_hsd.php">Course- by- Course Evaluation 
    Report (High School)</a></p>
    <p>If you have an Incomplete Education leading to a Bachelor’s Degree in 
    your home country and would like to finish or continue your education in the 
    US, you will need to have your Academic transcript/Official Listings 
    evaluated.<br>
    <font color="#0000FF"><i>Therefore order:</i></font><br>
    <a class="sublink" href="services_cce_ie.php">Course- by- Course Evaluation 
