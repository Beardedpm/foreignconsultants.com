      under average positions with the Associate or Bachelor’s Degrees 
      requirements<br>
      <font color="#0000FF"><i>Therefore order:</i></font><br>
      <a class="sublink" href="services_cpr.php">Comparability report</a></li>
      <li>Second, you can present a prospective employer your Evaluated Diploma(s) 
    and Academic Transcript(s) which is generally necessary if you pursuing the 
    average or over average positions with the Associate, Bachelor’s, Master’s, 
    PHD Degrees requirements to show your prospective employer the Listing(s) of 
    courses and (or) Certification exams taken, Grades obtained, Semester credit 
    or Units hours earned GPA, Master's or PHD thesis theme<br>
    <font color="#0000FF"><i>Therefore order:</i></font><br>
      <a class="sublink" href="services.php">Course- by- Course Evaluation Report (Associate, 
    Bachelor’s, Master’s Degree, PHD)</a></li>
    </ul>
