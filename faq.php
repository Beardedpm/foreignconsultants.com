    <p><b><font color="#800000" size="2"><a class="link" name="1"></a>What is the FCI Credential Evaluation 
    Service? </font></b></p>
    <p>Foreign Consultants, Inc. (FCI) is a US based Global company, 
    incorporated under the laws of the State of Illinois and licensed by the 
    City of Chicago since 1994, which provides Evaluation of Foreign Credentials 
    from all countries of the world assuring consistent treatment of those 
    educated outside the United States.<br>
    <br>
    We perform Evaluation, Translation, and Interpretation of Foreign 
    Credentials and related documents for individuals from abroad who wish to 
    use their knowledge and experience either in the United States or Worldwide.<br>
    <br>
    FCI provides an Evaluation of Foreign education in terms commensurable with 
    the US educational system. The Evaluation of Foreign Credentials executed by 
    FCI follows the placement recommendations approved&nbsp; by the
        <a class="sublink" target="_blank" href="http://www.ed.gov/NLE/USNEI/us/natcouncil-about.html">
        National Council on Evaluation of Foreign Credentials (CEC)</a>.<br>
    <br>
    The Council is the only interassociational body in the US specifically 
    created to review, modify, and approve placement recommendations written for 
    publications used by US admission community.<br>
    <br>
    FCI staff admits that the Educational Credentials Evaluation profession is a 
    self-disciplined profession, which, at best, abides by and complies with the 
    said guidelines and codes of ethics.</p>
    <p>[<a class="blue" href="#TOP">top</a>]</p>
    <p><b><font color="#800000" size="2"><a class="link" name="2"></a>What is credential evaluation?</font></b></p>
    <p>Credential evaluation is the process that converts records of education 
    completed in one country into its Foreign Equivalent. If you have completed 
    education somewhere other than the US and wish to have your credentials 
    understood and accepted on the territory of the US or by the US or other 
    International Employers Worldwide, you must have your Credentials evaluated.</p>
    <p>[<a class="blue" href="#TOP">top</a>] </p>
    <p><b><font color="#800000" size="2">
    <a class="link" name="3"></a>Why do I need to have my credentials 
    evaluated?</font></b></p>
    <p>You need to have your Credentials evaluated when you move from one 
    country to another and wish to have your Academic background recognized in 
    order to continue your education, apply for employment in the US or with the 
    International companies Worldwide, process your Immigration application in 
    the US or other countries or obtain a Professional license.</p>
    <p>[<a class="blue" href="#TOP">top</a>]</p>
    <p>
    <b><font color="#800000" size="2">
    <a class="link" name="4"></a>What does an evaluation report contain?</font></b></p>
    <p>There are two major types of the Evaluation reports:</p>
    <b>Comparability Report</b><ul>
      <li>Contains a description of the Credential presented including its 
        Title, Year awarded, Name of the institution attended, Major field of 
        study, and Foreign Degree awarded</li>
      <li>Provides with the approximate US Equivalent for each Credential.</li>
    </ul>
    <b>Course-by-Course Report</b><ul>
      <li>Contains a description of the Credential presented including its 
        Title, Year awarded, Name of the institution attended, Major field of 
        study, and Foreign Degree awarded.</li>
      <li>Lists all the Courses taken</li>
      <li>Hours or Credit hours attended with the equivalency to US Semester 
        credit or Unit credit hours.</li>
      <li>Grades earned with the equivalency to US Grading system</li>
      <li>GPA calculated</li>
      <li>Major/Minor field of studies established with the equivalency to US 
        educational standards</li>
      <li>Provides with the approximate US Equivalent(s) for each Credential</li>
    </ul>
